
public class 문자열_5 {

	public static void main(String[] args) {
		String s1 = "Hello ";
		String s2 = "Java!!";
		//문자열 결합
		System.out.println(s1 + s2);//사용빈도가 많다
		System.out.println(s1.concat(s2));
		//문자열 || , concat(s1,s2) => 오라클
		//Java : 5 / 2 = 2
		//Python : 5 / 2 = 2.5, 5//2 = 2
		
	}
	
}
